import ROOT as root

#no hgtd 20 GeV with W
c1 = root.TCanvas("noHGTD_20GeV_withW", "noHGTD_20GeV_withW")
c1.cd()
c1.SetLogy()

d = root.TFile("applied_noHGTD_combined9_energy_withW20GeV.root")
dt = d.Get("MVA")
dt.Draw("output_energy/(pt*cosh(eta))>>hd", "eta>2.5")
dh = root.gDirectory.Get("hd")
dh.SetLineColorAlpha(4,1)
dh.SetStats(False)
dh.Draw()

a = root.TFile("applied_noHGTD_max_energy_withW20GeV.root")
at = a.Get("MVA")
at.Draw("output_energy/(pt*cosh(eta))>>ha", "eta>2.5", "SAME")
ah = root.gDirectory.Get("ha")
ah.SetLineColorAlpha(1,1)
ah.SetStats(False)
ah.Draw("SAME")

