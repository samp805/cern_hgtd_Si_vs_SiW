bh.SetLineColorAlpha(2,1)
bh.SetStats(False)
bh.Draw("SAME")

c = root.TFile("applied_noHGTD_combined4_energy_withW20GeV.root")
ct = c.Get("MVA")
ct.Draw("output_energy/(pt*cosh(eta))>>hc", "eta>2.5", "SAME")
ch = root.gDirectory.Get("hc")
ch.SetLineColorAlpha(3,1)
ch.SetStats(False)
ch.Draw("SAME")

leg = root.TLegend(.1,.7,.48,.9)
leg.SetHeader("No HGTD Info, 20 GeV, SiW")
leg.AddEntry(dh, "Combined 9 Energy")
leg.AddEntry(ah, "Max Energy")
leg.AddEntry(bh, "Combined 2 Energy")
leg.AddEntry(ch, "Combined 4 Energy")
leg.Draw()

c1.SaveAs("noHGTD_20GeV_SiW.png")

